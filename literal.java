class literal{
    public static void main(String a[]){
        int num=0b1010; //binary literal
        System.out.println("Binary literal " + num);

        int num1 =0x1A; //hexadecimal literal
        System.out.println("Hexadecimal literal " + num1);

        char ch ='a';
        ch++;
        System.out.println("Character literal after increment: " + ch);
    }
}